let currentTourIndex = 0;
let currentSlide = 0;
let autoSlideInterval = null;

function showDetails(index) {
  currentTourIndex = index;
  currentSlide = 0;
  const tour = toursData[index];

  // Заголовок и описание
  document.getElementById('modal-title').textContent = tour.title;
  document.getElementById('modal-description').textContent =
    `${tour.description}\n\nЦена: ${tour.price}`;

  // Ссылка в WhatsApp
  const phone = "79052484096"; 
  const text = `Привет! Хочу забронировать экскурсию "${tour.title}"`;
  document.getElementById('whatsapp-link').href =
    `https://wa.me/${phone}?text=${encodeURIComponent(text)}`;

  // Карусель
  const carouselInner = document.getElementById('carousel-inner');
  const indicators = document.getElementById('carousel-indicators');
  carouselInner.innerHTML = '';
  indicators.innerHTML = '';

  tour.images.forEach((img, i) => {
    const slide = document.createElement('div');
    slide.className = 'carousel-item';
    
    const image = document.createElement('img');
    image.src = img;
    image.alt = `${tour.title} - фото ${i+1}`;
    
    // Обработка ошибок загрузки изображений
    image.onerror = function() {
      console.warn(`Не удалось загрузить изображение: ${img}`);
      // Можно добавить запасное изображение
      // this.src = 'image/placeholder.jpg';
    };
    
    slide.appendChild(image);
    carouselInner.appendChild(slide);

    const indicator = document.createElement('button');
    indicator.className = 'carousel-indicator' + (i === 0 ? ' active' : '');
    indicator.onclick = () => goToSlide(i);
    indicators.appendChild(indicator);
  });

  updateCarousel();
  document.getElementById('modal').style.display = 'flex';
  startAutoSlide();
}

function closeModal() {
  document.getElementById('modal').style.display = 'none';
  stopAutoSlide();
}

// Управление слайдами
function prevSlide() {
  currentSlide--;
  if (currentSlide < 0) {
    currentSlide = toursData[currentTourIndex].images.length - 1;
  }
  updateCarousel();
  resetAutoSlide();
}

function nextSlide() {
  currentSlide++;
  if (currentSlide >= toursData[currentTourIndex].images.length) {
    currentSlide = 0;
  }
  updateCarousel();
  resetAutoSlide();
}

function goToSlide(index) {
  currentSlide = index;
  updateCarousel();
  resetAutoSlide();
}

function updateCarousel() {
  const carouselInner = document.getElementById('carousel-inner');
  const indicators = document.querySelectorAll('.carousel-indicator');
  
  if (carouselInner) {
    carouselInner.style.transform = `translateX(-${currentSlide * 100}%)`;
  }
  
  indicators.forEach((ind, i) =>
    ind.classList.toggle('active', i === currentSlide)
  );
}

// ===== АВТОПРОКРУТКА =====
function startAutoSlide() {
  stopAutoSlide();
  autoSlideInterval = setInterval(() => {
    nextSlide();
  }, 4000);
}

function stopAutoSlide() {
  if (autoSlideInterval) {
    clearInterval(autoSlideInterval);
    autoSlideInterval = null;
  }
}

function resetAutoSlide() {
  stopAutoSlide();
  startAutoSlide();
}

// ===== МЕНЮ =====
function toggleMenu() {
  const navMenu = document.getElementById('navMenu');
  navMenu.classList.toggle('show');
}

// Закрытие модалки при клике вне
window.addEventListener('click', (e) => {
  if (e.target === document.getElementById('modal')) closeModal();
});

// Останавливаем автопрокрутку при наведении на карусель
document.addEventListener('DOMContentLoaded', function() {
  const carousel = document.querySelector('.carousel');
  if (carousel) {
    carousel.addEventListener('mouseenter', stopAutoSlide);
    carousel.addEventListener('mouseleave', startAutoSlide);
  }
});

// Подсветка активной ссылки в навигации
function setActiveLink() {
  const links = document.querySelectorAll('.nav a');
  const currentPage = window.location.pathname.split('/').pop() || 'home.html';

  links.forEach(link => {
    const linkPage = link.getAttribute('href');
    if (linkPage === currentPage) {
      link.classList.add('active');
    } else {
      link.classList.remove('active');
    }
  });
}

window.addEventListener('load', setActiveLink);
